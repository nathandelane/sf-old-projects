<?php

/**
 * LogStatus
 * This class represents the log status. These are just aliases for the current log statuses.
 * @author lanathan
 *
 */
final class LogStatus {
	
	const DEBUG = LOG_DEBUG;
	const INFO = LOG_INFO;
	const NOTICE = LOG_NOTICE;
	const WARNING = LOG_WARNING;
	const ERROR = LOG_ERR;
	const CRITICAL = LOG_CRIT;
	const ALERT = LOG_ALERT;
	const EMERGENCY = LOG_EMERG;
	
}

?>
<h1>Links to Interesting Stuff</h1>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.gnu.org/">The GNU Project</a>	is a global project to develop a free, as in freedom, operating system that anyone, anywhere can use and maintain all of their liberties and God-given freedoms and rights, including ownership of the software, allowances to modify or make changes to the software, and the freedom to sell it for their own profit, providing they include the GNU License and allow others the very same freedoms, liberties, and rights.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.fsf.org/">The Free Software Foundation</a> is a GNU-based organization devoted to promoting the GNU Operating System and protecting the rights, liberties, and freedoms set forth in the GNU General Public License, and associated licenses.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.stallman.org/">Richard Stallman's Personal Homepage</a>. Richard Stallman is the founder of the Free Software Foundation and the GNU Project. He is also a politically active figure, and a hero to many.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.research.att.com/~bs/">Bjarne Stroustrup's Home Page</a>. Bjarne Stroustrup is the inventor of C++, a widely used heavily typed high level programming language. He is regarded by many as one of the most influential men in the world of modern software development, as one can imagine.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.cs.helsinki.fi/u/torvalds/">Linus Torvalds' Home Page</a>. Linus Torvalds invented, developed, and maintains the Linux kernel, which is the core of the GNU Operating System. <a class="linkContainerLink" href="http://en.wikipedia.org/wiki/Linus_Torvalds">A Wikipedia page</a> was also developed in his name.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.linux.org/">Linux.org</a> is the best up to date resource on Linux, the GNU Operating System. Inclded are links to verious distributions, ISO CD and DVD image downloads, Linux news, supported hardware lists, and much more.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.ccil.org/jargon/jargon.html">The New Hacker's Dictionary</a> is a resource containing common to rare jargon, still much in use today among the Hacker (as in programmer) culture today. Included in those cultures might be AT&T, who is a direct successor to Ma Bell Labs, as one can see in the similarity of their logos.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://catb.org/esr/">Eric S. Raymond</a> is the author of many useful computer books and papers, including the New Hacker's Dictionary (above).
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.bsd.org/">BSD or Berkely Software Distribution</a> is a set of various Unix-based flavors of highly secure operating systems. The many variants of BSD, including FreeBSD, OpenBSD and NetBSD attempt to maintain an extremely Unix-similar (Bell Labs Unix, that is) architecture.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://gpl-violations.org/">GPL-Violations.org</a> maintains a list of companies who use GPL licensed software, but have violated the license agreements and are thus being sued because of those violations.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.nosoftwarepatents.com/en/m/intro/index.html">NoSoftwarePatents.com</a> is a website devoted to to action to eliminate software patents in the world. Software patents cause serious problems and controversy in the United States of America, and other countries have taken steps toward creating software patents. However software patents are not very helpful except to cause others pain. They also violate the rights of developers and users who use open source software.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://endsoftpatents.org/">End Software Patents</a> is an action to end the use and abuse of software patents in the United States of America.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.gnewsense.org/">GNewSense</a> is the latest Official FSF-supported Linux distribution. It is the official GNU Operating System.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.ubuntu.com/">Ubuntu Linux</a> is one of the best and most easy to use desktop Linux distributions available. Ubuntu used to be the FSF-supported distribution, however because they support non-free and universe repositories in their default distribution, Richard Stallman chose no longer to support it as the GNU Operating System.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.sourceforge.net/">Sourceforge.net</a> is one of the largest hosts of open source software. It is my first resource when I am looking for a specific piece of software. I also participated in one project on Sourceforge.net, namely KPC, for which I designed and wrote the GUI front-end.
</div>
<div class="linkContainer">
	<a class="linkContainerLink" href="http://www.java.net/">Java.net</a> is an official Java developer community from Sun Microsystems. They host both individual and team Java-based development projects.
</div>


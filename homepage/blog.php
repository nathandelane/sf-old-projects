				<h3>Codes..Cryptography..Secrets</h3>
				<div>
					<div>
						All my life I have been intrigued by secrets. Only later in my life did I learn that they were as real as they were. As a kid of eight or nine, I dabbled in writing short programs that would transform my text based files into something else. A little later on I learned that compression was a form of encryption. But I never really learned how to do anything but heuristic compression, which is based on finding the most common character down to the least common character. And I suppose that if all characters are close to equally as common as each other, then compression using this method is not very effective. *LOL, and maybe that's why compressing images and executable files does little, if anything, to reduce the size.
					</div>
					<br />
					<div>
						But from Junior Hight School and on up, I have tried to come up with ways to send secret messages to my friends. One of my later inventions was a simple binary breakdown of only letters contained in a textual message. I don't remember the discrete semantics, but the general idea was this:
					</div>
					<br />
					<div>
						BEBCBCDEACDCEBCEABEACDEBECDCEABEEBCEDCEADACDCEADABEBCEACEDEB
					</div>
					<br />
					<div>
						It contains a binary representation of just the lowercase or uppercase alphabet, but not both (that would give too much away), and there are no spaces, and is no punctuation throughout. Think that's difficult? I doubt it. Other than a couple of instances when it isn't obvious that a representation ends, this code is relatively simple to crack. But I won't tell you how.
					</div>
					<br />
					<div>
						A while back (several years) I came up with this one than one of my best friends ever and I used to write in back and forth. We have both since moved on, and she wasn't that good of a friend in recent times, and was jealous, and hurt I suppose, but I still regard her as one of the best things in my life before I was married. Here's an example of our code. I called it Luniseau /loon i'see ou/.
					</div>
